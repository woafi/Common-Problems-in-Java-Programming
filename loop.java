import java.util.*;
public class loop {
    public static void main(String[] args) {
        Scanner sc =new Scanner(System.in);
        String text = sc.nextLine();
        for (int i=0; i<4; i++ ) { 
            for (int j=0; j<4; j++) {
                System.out.print(text);
            }
            System.out.print("\n");
            }
        }

    }
    

    